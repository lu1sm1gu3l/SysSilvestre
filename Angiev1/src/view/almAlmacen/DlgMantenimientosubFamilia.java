    private void guardar()

    {
        ovalidar.validarContorles();
        if(ovalidar.getisVacio()){

            int resul=JOptionPane.showConfirmDialog(this, "¬øEsta seguro de Guardar? ", "Dialogo de Confirmaci√≥n", JOptionPane.YES_NO_OPTION);
                if (resul==JOptionPane.YES_OPTION) {
                    CESubfamilia oCESubfamiliaTmp=new CESubfamilia();
                    oCESubfamiliaTmp.setDescripcion(TxtDescripcion.getText());

                    if(opcionABM==1){

                        oCESubfamiliaTmp.setIdFamilia(oCEFamilia.getIdFamilia());
                        oCESubfamiliaTmp.setCodigo(oCEFamilia.getCodigo()+TxtCodigo.getText());

                    }else{oCESubfamiliaTmp.setIdSubFamilia(this.oCESubfamilia.getIdSubFamilia());}


                        if(CCSubFamilia.ABMSubFamilia(opcionABM, oCESubfamiliaTmp)==1){
                       // JOptionPane.showMessageDialog(null,"se Guardo Con exito");
                            dispose();
                        }else{
                        JOptionPane.showMessageDialog(null, "Operaci√≥n Fallida");
                        }
            }

            } else{
            TxtCodigo.requestFocus();
            JOptionPane.showMessageDialog(null,"Verificar Datos");

            }



    }
    private void TxtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtCodigoKeyPressed
        if(evt.getKeyCode()==evt.VK_DOWN||evt.getKeyCode()==evt.VK_ENTER) {
            TxtDescripcion.requestFocus();
        }

    }//GEN-LAST:event_TxtCodigoKeyPressed

    private void TxtDescripcionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtDescripcionKeyPressed
        if(evt.getKeyCode()==evt.VK_DOWN||evt.getKeyCode()==evt.VK_ENTER) {
            btnGuardar1.requestFocus();
        }
        if(evt.getKeyCode()==evt.VK_UP) {
            TxtCodigo.requestFocus();
        }
    }//GEN-LAST:event_TxtDescripcionKeyPressed

    private void btnGuardar1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnGuardar1KeyPressed
       if(evt.getKeyCode()==evt.VK_UP||evt.getKeyCode()==evt.VK_LEFT||evt.getKeyCode()==evt.VK_DOWN||evt.getKeyCode()==evt.VK_RIGHT) {
          TxtDescripcion.requestFocus();
      }
       if(evt.getKeyCode()==evt.VK_ENTER) {
          guardar();
      }
    }//GEN-LAST:event_btnGuardar1KeyPressed


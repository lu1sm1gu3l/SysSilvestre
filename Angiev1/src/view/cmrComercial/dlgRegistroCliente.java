    private void  guardar()
    {
    CECliente oCEClienteAux= new CECliente();
    oCEClienteAux.setApellidoMaterno(TxtApellidoMaterno.getText().trim());
    oCEClienteAux.setCorreo(TxtCorreoElectronico.getText().trim());
    oCEClienteAux.setCodigo("");
    oCEClienteAux.setDNI(TxtDNI.getText().trim());
    oCEClienteAux.setDireccion(TxtDireccion.getText().trim());
    oCEClienteAux.setNombres(TxtNombre.getText().trim());
    oCEClienteAux.setApellidoPaterno((TxtApellidoPaterno.getText().trim()));
    oCEClienteAux.setTelefono(TxtTelefono.getText().trim());
    oCEClienteAux.setRUC(TxtRuc.getText().trim());
    oCEClienteAux.setCelular(TxtCelular.getText());

      if(RbtnFemenino.isSelected())
      {
          oCEClienteAux.setSexo("F");
      }
          else
      {
        if(RbtnMaculino.isSelected())
        {
            oCEClienteAux.setSexo("M");
        }
        
      }
    if(RbtnPersonaJuridica.isSelected())
      {
          oCEClienteAux.setIdTipoPersona(2);
          oCEClienteAux.setSexo(null);
      }
          else
      {
          oCEClienteAux.setIdTipoPersona(1);
      }

    
    oCEClienteAux.setIdSucursal(1);

    ArrayList<CECliente> olista=new ArrayList<CECliente>();
        //Al momento de guardar un dato
       if(OperacionAMB==1)
        { 
           int resul=0;
               olista.add(oCEClienteAux);
                resul=CCCliente.INSCliente(olista);
                if(resul!=0)
                {
                    JOptionPane.showMessageDialog(null,"Operacion Exitosa");                    
                   OperacionAMB=0;
                   dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Operaci√≥n Fallida");
                }

            
        }
        else
        {  //Al momento de editar
            if(OperacionAMB==2)
            {
                 int resul=0;

                oCEClienteAux.setIdCliente(oCECliente.getIdCliente());
                olista.add(oCEClienteAux);
                resul=CCCliente.UPDCliente(olista);
                  if (resul!=0) {
                      JOptionPane.showMessageDialog(null,"Operacion Exitosa");
                      OperacionAMB=0;
                      dispose();
                    }
                else
                   {
                   JOptionPane.showMessageDialog(null, "No se pudo completar la operaci√≥n...!");
                   }

            }
        }

    }
